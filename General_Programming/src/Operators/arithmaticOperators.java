package Operators;

public class arithmaticOperators {

	public static void main(String[] args) {
		
		/* Arithmetic operators
		 * 1. addition 2. subtraction 3.multiplication 4.division 5. modulus 
		 */
		int number_one=10; //variable one
		int number_two=20; //variable two
		
		//addition
		System.out.println("Additon of 10 and 20 are: " + (number_one+number_two));
		//subtraction
		System.out.println("Difference of 10 and 20 are: " + (number_one-number_two));
		//multiplication
		System.out.println("Multiplication of 10 and 20 are :" + number_one*number_two);
		//division
		System.out.println("Division of 10 and 20 are :" + number_one/number_two);
		//modulus
		System.out.println("Modulo of 10 and 20 are :" + number_one%number_two);
		
		}
}